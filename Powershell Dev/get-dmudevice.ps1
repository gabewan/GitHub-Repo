function Get-DMUdevice ($username) {
Set-Location '000:'
Get-CMUserDeviceAffinity -UserName $username | ?{$_.Sources -contains '4'} | %{Get-CMDevice -ResourceId $_.ResourceID  | select -ExpandProperty Name}
}
