function Get-DevicePing{
$names = Get-content "c:\text.txt"
$ip = Get-NetIPAddress -CimSession $names -AddressFamily IPv4

foreach ($name in $names){
  if (Test-Connection -ComputerName $name -Count 1 -ErrorAction SilentlyContinue){
    Add-Content c:\users\grlewis\desktop\output.csv "$name,up,$ip"
  }
  else{
    Write-Host "$name,down"
  }
}
}
