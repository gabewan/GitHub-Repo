function Get-WaitOnPC 
{

$cn = read-host "Enter Device Name:"
    do {$ping = test-connection -comp "$cn" -Quiet} 
        until ($ping -eq "true")
            Send-MailMessage -To Gabe.lewis@nghs.com -From Gabe.Lewis@nghs.com -Subject "$cn is now online" -Body "$cn has been detected online" -SmtpServer mail.nghs.com
}


